int CubeMesh::VertexCount()
{
	return static_cast<int>(sizeof(vertices) / sizeof(*vertices));
}

Point CubeMesh::GetVertex(int i)
{
	return vertices[i];
}

Vector CubeMesh::Dimensions()
{
	return Vector(2, 2, 2);
}

Point CubeMesh::Center()
{
	return Point(0, 0, 0);
}

int CubeMesh::FaceCount()
{
	return static_cast<int>(sizeof(faces) / sizeof(*faces));
}

CubeMesh::Face CubeMesh::GetFace(int i)
{
	return faces[i];
}

int CubeMesh::EdgeCount()
{
	return static_cast<int>(sizeof(edges) / sizeof(*edges));
}

CubeMesh::Edge CubeMesh::GetEdge(int i)
{
	return edges[i];
}
